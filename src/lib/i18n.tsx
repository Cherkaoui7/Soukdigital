type I18nContextValue = {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
};

const I18nContext = createContext<I18nContextValue | null>(null);

const STORAGE_KEY = "souk.locale";

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("fr");

  useEffect(() => {
    if (typeof window === "undefined") return;
    const saved = window.localStorage.getItem(STORAGE_KEY) as Locale | null;
    if (saved && ["fr", "ar", "en"].includes(saved)) setLocaleState(saved);
  }, []);

  useEffect(() => {
    if (typeof document === "undefined") return;
    const html = document.documentElement;
    html.lang = locale;
    html.dir = locale === "ar" ? "rtl" : "ltr";
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale: (l) => {
      setLocaleState(l);
      if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, l);
    },
    t: (key) => dictionaries[locale][key] ?? dictionaries.fr[key] ?? key,
    dir: locale === "ar" ? "rtl" : "ltr",
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}

export function localizedField<T extends { name_fr: string; name_ar: string; name_en: string }>(
  row: T,
  locale: Locale,
  field: "name" | "description" = "name",
): string {
  const key = `${field}_${locale}` as keyof T;
  const raw = row[key];
  if (typeof raw === "string" && raw.length > 0) return raw;
  const fallback = row[`${field}_fr` as keyof T];
  return typeof fallback === "string" ? fallback : "";
}
